package basiscPrograming;

public class BreakAndContinueDemo {

    public static void main(String[] args) {

        // Continue
        System.out.println("debug");

        for( int i =1 ; i<=10 ; i++){

            if(i%2==0){
                break;
            }
            System.out.println(i);
            //Added
        }


    }
}
